              <h3>Proses Pembuatan Gantungan Rumah Ukir</h3>
            </article>
        <div class="clear"></div>
        <ul class="thumbnails thumbnails-1 list-services">
            
            <h5>Bahan :</h5>
1.	Kayu/papan bekas <br>
2.	Pensil <br>
3.	Lem kayu<br>
4.	Gergaji ukiran/triplex <br>
5.	Amplas kasar dan halus <br>
6.	Cat/thiner /pernis<br>
7.	Bor tangan + mata bornya <br>
8.	  Pisau raut<br>
9.	Kikir<br>
10.	Kuas<br>
11.	Tang<br>
12.	Gantungan kunci (Ring)<br>

<br>
<h5>Langkah-langkah pengerjaan :</h5> 

1. Buat pola kata/nama atau bentuk hewan, dan lain-lain pada papan/kayu. Usahakan pada tahap ini kata atau bentuk yang di gambar harus benar benar rapi dan unik.<br><br>

2. Setelah di gambar, gergaji kayu tersebut sesuai garis gambar, jangan sampai tidak rapi. Lalu buat background untuk nama atau gambar tersebut lalu beri lubang dengan menggunakkan &nbsp;&nbsp;&nbsp;&nbsp;bor dibagian ujung background kayu untuk menggantungkan ring gantungan kunci.<br><br>

3. Setelah pola di kayu sudah di gergaji maka tahap selanjutnya bagian bagian kayu nya kita amplas dahulu dengan menggunakkan amplas kasar untuk meratakan bagian kayu yang tidak &nbsp;&nbsp;&nbsp;&nbsp;rata.<br><br>

4. Setelah rata di amplas dengan menggunakkan amplas kasar, maka amplas lagi semua bagian kayu tersebut dengan menggunakkan amplas yang halus ini bertujuan agar bagian kayu &nbsp;&nbsp;&nbsp;&nbsp;terlihat rapi, rata, dan mulus.<br><br>

5. Setelah semua selesai di amplas, maka proses selanjutnya yaitu dengan menempelkan nama atau gambar kayu dan background kayu tersebut menggunakkan lem kayu, usahakan lem &nbsp;&nbsp;&nbsp;&nbsp;memenuhi kedua bagian belakang kayu tersebut hingga merata.<br><br>

6. Setelah ditempelkan, diamkan dahulu kayu yang sudah ditempel tersebut hingga benar benar kering, supaya daya rekatnya sempurna dan kuat.<br><br>

7. Setelah kering dan benar-benar menempel dengan kuat, kita beri pernis atau pylox yang berwarna clear/ bening atau transparan. Usahakan jangan terlalu dekat dan jangan terlalu banyak. <br><br>

8. Terakhir kita pasangkan ring gantungan kunci memakai tang pada ujung kayu atau gantungan yang sudah di lubangi<br><br>

